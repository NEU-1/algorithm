import sys
input = sys.stdin.readline

for _ in range(int(input())):
    n = int(input())
    post = [list(map(int,input().split())) for _ in range(2)]
    dp = [[0]*n for _ in range(2)]

    dp[0][0] = post[0][0]
    dp[1][0] = post[1][0]

    if n == 1:
        print(max(dp[0][-1],dp[1][-1]))

    if n == 2:
        dp[0][1] = dp[1][0]+post[0][1]
        dp[1][1] = dp[0][0]+post[1][1]
        print(max(dp[0][-1],dp[1][-1]))

    if n >= 3:
        dp[0][1] = dp[1][0]+post[0][1]
        dp[1][1] = dp[0][0]+post[1][1]

        for i in range(2,n):
            dp[0][i] = max(dp[1][i-1]+post[0][i], dp[1][i-2]+post[0][i], dp[0][i-2]+post[0][i])
            dp[1][i] = max(dp[0][i-1]+post[1][i], dp[0][i-2]+post[1][i], dp[1][i-2]+post[1][i])

        print(max(dp[0][-1],dp[1][-1]))

